namespace MidiMusicTools.Enum {
  /*
   * Description: The values of this enumerator contain strings of characters, 
   * with each character representing a track to be created of a certain type.
   * 
   * Track types:
   * M: Melody
   * C: Chorus
   * B: Bass
   */
  public enum TrackType
  {
    Melody,
    Chord,
    Bassline
  }
}
